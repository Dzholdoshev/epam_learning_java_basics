package chapter2_Data_types.primitives;

public class Literal_with_a_point {

   // With a fixed point:
    double var9 = 1.618;
   // In exponential form (floating point):
    double var10 = -0.112E-05;
   // When it is necessary to specify a literal of the type float,
   // after the last digit use the character F or f as a suffix:
    float var11 = -18.456F;
}
